// - Créez une variable `month` de valeur `"January"`
// - À l'aide d'un switch, affichez "Winter", "Spring", "Summer" ou "Fall" selon la valeur du mois
// - Changez la valeur de `month` pour changer le résultat


var month = "september";

switch (month){

    case "january":
        console.log("winter");
        break;

    case "march":
        console.log("spring");
        break;

    case "june":
        console.log("summer");
        break;

    case "september":
         console.log("fall");
         break;

    default:
        console.log("la valeur de ta variable ne correspond à aucune de tes réponses.")
   
}



